

export const formatDateForDisplay = (isoDate: string): string => {
  if (!isoDate || !/^\d{4}-\d{2}-\d{2}(T.*)?$/.test(isoDate)) return isoDate;
  const datePart = isoDate.split('T')[0];
  const [year, month, day] = datePart.split('-');
  return `${day}-${month}-${year}`;
};

export const formatDateForStorage = (displayDate: string): string => {
  if (!displayDate || typeof displayDate !== 'string') return displayDate;
  // Handles DD-MM-YYYY, DD.MM.YYYY, DD/MM/YYYY
  const parts = displayDate.match(/^(\d{1,2})[-/.](\d{1,2})[-/.](\d{4})$/);
  if (parts) {
    const [, day, month, year] = parts;
    return `${year}-${String(month).padStart(2, '0')}-${String(day).padStart(2, '0')}`;
  }
  // Handles YYYY-MM-DD
  const isoParts = displayDate.match(/^(\d{4})[-/.](\d{1,2})[-/.](\d{1,2})$/);
  if (isoParts) {
    const [, year, month, day] = isoParts;
    return `${year}-${String(month).padStart(2, '0')}-${String(day).padStart(2, '0')}`;
  }
  return displayDate; // Return original if format doesn't match known patterns
};


